// match.dart
class Match {
  final String date;
  final String opponent;
  final int goals;
  final int assists;
  final int yellowCards;
  final int redCards;

  Match({
    required this.date,
    required this.opponent,
    required this.goals,
    required this.assists,
    required this.yellowCards,
    required this.redCards,
  });
}
